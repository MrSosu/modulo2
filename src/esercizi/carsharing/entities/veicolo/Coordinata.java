package esercizi.carsharing.entities.veicolo;

public final class Coordinata {

    private Double x;
    private Double y;

    public Coordinata(Double x, Double y) {
        this.x = x;
        this.y = y;
    }

    public Double getX() {
        return x;
    }

    public Double getY() {
        return y;
    }
}
